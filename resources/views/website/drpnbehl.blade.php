                     <p><b><i>He believed many skin disorders get precipitated because of pollutants in the environment or chemical contamination of the body in food or surroundings.
                     </i></b></p>
                     <p>Dr. Behl donated his private earnings and any funds generated from his work without taking any personal benefits for himself or his family. 
