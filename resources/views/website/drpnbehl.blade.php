                     <p><i><b>He was rightly called the "Father of Dermatosurgery” in India by the Association of Cutaneous Surgeons of India at Jodhpur in the year 2000. </b></i></p>
                     <p> He was welcomed and celebrated in the traditional way by wearing a Rajasthani Turban. Other than this, he was countlessly awarded for his pioneering knowledge and practical work in dermatology. <br>
                        Being a philanthropist at heart, he opened a free dermatologic clinic in the Santhal Tribal Belt of Bihar to provide treatment to remote villagers. Additionally, he organized over 30 free dermatology treatment camps in remote areas of Rajasthan, Uttar Pradesh, Haryana, Jammu and Kashmir, and Bihar, where skin diseases were widespread. He used to travel all over the world as an invited speaker to mesmerize the delegates at international conferences with his extremely well-versed knowledge of the subject. His advice on the elimination of the root cause of skin disease and its therapy was always scientific and thought-provoking. - (photo with camp)</p>
                     

                     <p>The publishers, M/s Minerva Press in the U.K., described his experience as follows: “Kidnapped and held for fifteen days in a chilly sugarcane field in 1994, Dr. Behl, an eminent dermatologist, followed in the great tradition of John Bunyan and other captives who pit their minds against their circumstances—and win.” <br>
                        He always had dreamt of an independent autonomous charitable institution of Dermatology, which took no time to become a reality in the form of an establishment in Greater Kailash-1 with the name of Skin Institute and School of Dermatology. <br>
                        Dr P.N. Behl has also founded the Behl Public School in Dasna, Ghaziabad. It had started initially with traditional methods of education but now has since evolved significantly under the leadership of Mrs Priya Behl, CEO at Dr Behl Skin Hospital, SISD. The school has upgraded its infrastructure, to enhance the learning experience for its students. This progression reflects its commitment to providing quality education and preparing students for the challenges of the modern world. Behl Public School now accommodates over more than 400 students. It has embraced modern advancements such as computerization and the establishment of specialized labs to enhance the educational experience. Mrs Priya Behl continues to guide the school towards achieving academic excellence and fostering the overall holistic development of its students, following the vision, mission and footprints of Dr P. N. Behl, the leadership has now been taken forward by her. <br>
                        
