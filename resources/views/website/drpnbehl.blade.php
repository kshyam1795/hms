               <h3 class="ats-txt-center">Founder<br> 
                  Late (Prof.)  Dr. P.N.Behl, </h3>
               <h4 class="ats-txt-center">[23-09-1923 to 15-10-2002]</h4>
               <p class="ats-txt-center">‘‘Those who knew him, Would not be able to define his greatness. 
                Those who feel they defined it, Could not have known him.
               ’’</p>
               
               <p>Professor (Dr.) Pran Nath Behl, MB, F.R.C.P. (Edinburgh), FICAI, and FIAMS, <br>
                  Fellow of the American Academy of Dermatology, <br>
                  Fellow of the American Society of Dermatologic Surgery, <br>
                  Fellow of the International Society of Cutaneous Surgery, <br>
                  Past Vice President of the International Society of Dermatology, <br>
                  Founder of SISD (Skin Institute and School of Dermatology). 
                  </p>
                  <p>
                     <b>Prof. Pran Nath Behl</b> was born on September 23, 1923, and received his initial education in Amritsar. He then enrolled at KE Medical College in Lahore, the only medical institution in the province at that time. In May 1946, he graduated with an MBBS degree. Then he began working as a house physician at Sir Ganga Ram Hospital in Lahore.
                  </p>
                  <p>However, the partition of the country compelled him to migrate first to Delhi and then to Bombay. Fortunately, his optimism and determination paid off, and he had the opportunity to study and work in England to support his education.
                  </p>
                  <p><b>
                     He trained for postgraduation at St. John’s Hospital for Diseases of the Skin, London from 1947-51. He successfully passed the Membership exam after four years of hard work, at the Royal College of Physicians in Edinburgh in September 1951, obtaining his MRCP in 1951 from Edinburgh, specializing in dermatology and FRCP from Scotland in 1963. He practised as a family doctor under the National Health Services from 1948-51.</b></p>
                  <p>
                     During this time, he faced numerous challenges, including the struggle for independence, societal divisions, and financial difficulties. Nonetheless, the dedication of his teachers and the disciplined approach of his fellow students helped him become a capable and responsible doctor. He had occupied a distinguished position among the leading physicians in India.</p>   
                  <p>
                     However, he is remembered as the first dermatologist in India to establish a private institute for skin, “The Skin Institute and School of Dermatology” now known as with the name of  Dr. Behl’s, SISD (Skin Institute and School of Dermatology) Greater Kailash, New Delhi.                                                                                                        Earlier, The Skin Institute was established on March 1, 1965, at 13 Daryaganj, New Delhi. 

                  </p>
                   <p><b>
                     Dr P.N. Behl was a world-renowned dermatologist and specialist in cutaneous surgery. He was the Head of the Department of Dermatology at Maulana Azad Medical College. He held many prime positions at prestigious colleges and institutes like;    
                  </b></p>
                  <p><u>He was appointed for and held the various positions as:-</u></p> 
                  <ul>
                     <li>Director of Skin Institute and School Of Dermatology, New Delhi</li>
                     <li>Director, Rural Cum Industrial Skin & Health Institute, Dasna,</li>
                     <li>Director & Founder, P.N. Behl Foundation, New Delhi</li>
                     <li>President, Leprosy Rehabilitation Society, Bethany Village, New Delhi</li>
                     <li>Formerly Lecturer, College Of Nursing and Jawaharlal Nehru Occupation Therapy Institute</li>
                     <li>Honorary Dermatologist, Holy Family Hospital,</li>
                     <li>Honorary Professor and Head of the Department of Dermatology, Maulana Azad Medical College & Irwin Hospital, Delhi.</li>
                     <li>Visiting Lecturer in Dermatology, Lady Hardinge Medical College, New Delhi</li>
                     <li>Ex-examiner, Bombay, Delhi, Odisha, Patna and Calcutta Universities, etc.</li>
                     <li>Founder Fellow, Indian College of Allergy & Applied Immunology</li>
                     <li>Past President, dermatological Society of India,</li>
                     <li>Fellow Dermatological Society of France, Iran, Poland, etc.</li>
                     <li>Editor, Dermatology Times</li>
                     <li>President, Skin Institute, Research Society</li>
                     <li>President of Leprosy Institute for Rehabilitation</li>
                     
                  </ul> 
                  <p><u>He delivered talks in Dermatology Department in June 1993 on the following subjects:-</u></p>
                  <ul>
                     <li>Vitiligo update in management and surgical approach</li>
                        <li>Panorama of dermatology</li>
                        <li>Herbs Useful in Dermatological Therapy</li>
                        <li>Laser.</li> 
                        
                  </ul>
                  <p><b>The Association of Cutaneous Surgeons (India) (ACSI)</b> has instituted an oration in the name of Dr. Pran Nath Behl, the father of dermatologic surgery in India. Dr. PN Behl is widely credited as the original describer of vitiligo surgery. He described Thin Thiersch Grafts for stable vitiligo as early as 1963- which was a landmark in the history of vitiligo surgery and performed thousands of such surgeries. He described the criteria for stability of vitiligo based on clinical and histological criteria. At a time when dermatosurgery was almost non-existent in India, he was a pioneer not only in vitiligo surgery but also in several other fields, such as dermabrasion and the treatment of keloids. </p>
                  <p>Shortly thereafter, he married a Welsh woman, Marjorie Marie Hopkins, with the consent of both sets of parents.</p>
                  <p>His pioneering work in skin surgery attracted numerous patients from both India and abroad, ensuring that the indoor wards were often full. The large outpatient department provided extensive exposure to postgraduate doctors through their regular lectures, assisting them in skin surgeries, and managing challenging cases, along with the use of advanced diagnostic and therapeutic technologies. This facility offered missionary service, free treatment for the poor, free medications, independent histopathology, and an advanced scientific research department, all under one roof, along with a 30-bed indoor ward. </p>
                  <p><b><i>Patients suffering from major chronic disorders used to be referred to Dr. P.N. 
                     Behl unhesitatingly by many dermatologists.
                     </i></b></p>
                  <p>He stressed the importance of applying time and effort to ensure no patient remained undiagnosed.</p>
                  <p><b>He has given an essential three-step formula which was inclusive of morphological diagnosis, clinical diagnosis and etiological diagnosis to manage skin disorders.</b></p>
                  <p>He believed that no disease is truly idiopathic; rather, it requires more effort to uncover the cause. He truly believed that to give long-lasting relief to the patient it is important to eliminate the cause, then treat the diseases with as minimal medication as possible and also guide them to prevent the disease in future. He always discouraged the irrational use of medications and unnecessary investigations. His approach to life was holistic.
                  </p>
                  <p><b><u>He cultivated his approach further in his two ventures, named </u></b></p>
                  
                  <ul>
                     <li>Rural-Cum-Industrial Skin and Health Institute, Dasna in Ghaziabad, and</li>
                     <li>Dr. Behl Holistic Health Institute, Ashok Vihar in Delhi.</li>
                  </ul>
                  <p><u><b>He was the Founder of:-</b></u></p>
                  <ul>
                     <li>Skin Institute and Public Service Charitable Trust</li>
                     <li>Skin Institute Research Society</li>
                     <li>Dr. P.N. Behl Foundation</li>
                  </ul>

                  <p>As an eminent dermatologist, he travelled extensively, delivering lectures on Dermatology and Dermatosurgery in the United States, USSR, Europe, China, Africa, Japan, and the Far East. <br>
                     As he sailed back to Bombay, he realized that establishing a relatively new speciality independently was quite challenging. <br>
                     He secured an honorary appointment as a Dermatology Professor at <b>Irwin Hospital</b> in <b>New Delhi</b> through his hard work, grit, confidence and persuasive abilities. <br>
                     He was a One-Man department with a single room in the outpatient block on the first floor. Through his hard work, he raised awareness that skin afflictions can be treated specifically and prevented through certain measures. He turned his speciality into a highly sought entity. In due course, he created an independent, large outpatient service and indoor wards for the admission of skin patients, along with several postgraduate doctors and paramedics</p>
               <p> <b>
                  He introduced Skin grafting in stable vitiligo for the first time in the world in the year of 1964. He and his team under his guidance used to perform many routine surgeries for Debridement and grafting of chronic non-healing ulcers, Dermabrasion of lichen amyloidosis, leprosy deformity, and smallpox and acne scars, Excision of Keloids followed by Grenz ray irradiation.   
               
               </b></p>
               <p><u>For post-graduate education and family practitioners, he initiated two full-time courses:</u></p>
               <ul>
                  <li>M. Derma (2 years) and </li>
                  <li>Fellow of Skin Institute (FSI) (1 year) at Skin Institute & School of Dermatology (SISD). </li>
               </ul>
               <p>The National Board of Examination recognised four seats for the prestigious DNB course at his institution. <br> 
                  Every year he used to conduct the 4-day annual/biannual workshop, where he used to impart cosmetic, therapeutic and surgical skills to raise skin specialists. <br>
                  Other than that, there were about over 150 (one hundred and fifty) students who had successfully completed DNB of 2 years and 3 years and also M. Derma of full-time course of two years in Dermatology who had served not just in India but abroad as well.
                  </p>

                  <p>Over his illustrious career, he was <b><i>a prolific writer, he authored many books on dermatology which have trained MBBS students, postgraduates, and dermatologists for over five decades and over 300 articles of national and international repute, addressing significant medical, scientific, and social issues.
                  </i></b></p>

                  
